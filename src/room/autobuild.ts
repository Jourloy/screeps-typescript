	private static createConstructions(
		room: Room,
		structures: {
			[name: string]: {
				pos: { x: number; y: number }[];
			};
		}
	) {
		for (let i in structures) {
			for (let j in structures[i].pos) {
				// @ts-ignore
				room.createConstructionSite(structures[i].pos[j].x, structures[i].pos[j].y, i);
			}
		}
	}

