	private static checkStructures(room: Room, amountStructures: AmountStructures) {
		const structures = RoomCollector.info[room.name];
		
		let spawnsState;
		let extensionsState;
		let towersState;
		let storageState;
		let terminalState;
		let containerState;

		if (amountStructures.spawns) {
			const spawns = structures.allStructures.filter(s => s.structureType === 'spawn');
			if (spawns.length < amountStructures.spawns) spawnsState = false;
			else spawnsState = true;
		} else spawnsState = true;

		if (amountStructures.extensions) {
			const extensions = structures.allStructures.filter(s => s.structureType === 'extension');
			if (extensions.length < amountStructures.extensions) extensionsState = false;
			else extensionsState = true;
		} else extensionsState = true;

		if (amountStructures.towers) {
			const towers = structures.allStructures.filter(s => s.structureType === 'tower');
			if (towers.length < amountStructures.towers) towersState = false;
			else towersState = true;
		} else towersState = true;

		if (amountStructures.storage) {
			if (!room.storage) storageState = false;
			else storageState = true;
		} else storageState = true;

		if (amountStructures.terminal) {
			if (!room.terminal) terminalState = false;
			else terminalState = true;
		} else terminalState = true;

		//if (basicStructures.container) {
		//    if (!room.terminal) terminalState = false;
		//    else terminalState = true;
		//} else terminalState = true;

		if (
			!spawnsState ||
			!extensionsState ||
			!towersState ||
			!storageState ||
			!terminalState ||
			!containerState
		) {
			this.run(room);
		}
	}

